package com.nari.orderlypower;

public class CtrlParamBean {
	private String orgNo;
	private String consNo;
	private String tmnlAssetNo;
	private Short totalNo;
	private Short mpSn;
	private String protocolCode;

	public String getOrgNo() {
		return orgNo;
	}

	public void setOrgNo(String orgNo) {
		this.orgNo = orgNo;
	}

	public String getConsNo() {
		return consNo;
	}

	public void setConsNo(String consNo) {
		this.consNo = consNo;
	}

	public String getTmnlAssetNo() {
		return tmnlAssetNo;
	}

	public void setTmnlAssetNo(String tmnlAssetNo) {
		this.tmnlAssetNo = tmnlAssetNo;
	}

	public Short getTotalNo() {
		return totalNo;
	}

	public void setTotalNo(Short totalNo) {
		this.totalNo = totalNo;
	}

	public String getProtocolCode() {
		return protocolCode;
	}

	public void setProtocolCode(String protocolCode) {
		this.protocolCode = protocolCode;
	}

	public Short getMpSn() {
		return mpSn;
	}

	public void setMpSn(Short mpSn) {
		this.mpSn = mpSn;
	}

	@Override
	public String toString() {
		return "orgNo=" +orgNo+",consNo="+consNo+",tmnlAssetNo="+tmnlAssetNo +",totalNo="+totalNo+",protocolCode="+protocolCode;
	}
}
