package com.nari.terminalparam;

/**
 * 终端变更信息数据值对象
 * @author 姜炜超
 */
public class TerminalChgInfoBean implements java.io.Serializable {
	private String tmnlAddr;//终端地址
	private String status;//状态
    private String newTmnlAddr;
    private String tmnlTaskTypeName;
    private String tmnlTaskType;
	
	public String getTmnlAddr() {
		return tmnlAddr;
	}
	public void setTmnlAddr(String tmnlAddr) {
		this.tmnlAddr = tmnlAddr;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getNewTmnlAddr() {
		return newTmnlAddr;
	}
	public void setNewTmnlAddr(String newTmnlAddr) {
		this.newTmnlAddr = newTmnlAddr;
	}
	public String getTmnlTaskType() {
		return tmnlTaskType;
	}
	public void setTmnlTaskType(String tmnlTaskType) {
		this.tmnlTaskType = tmnlTaskType;
	}
	public String getTmnlTaskTypeName() {
		return tmnlTaskTypeName;
	}
	public void setTmnlTaskTypeName(String tmnlTaskTypeName) {
		this.tmnlTaskTypeName = tmnlTaskTypeName;
	}
}
