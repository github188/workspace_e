package com.nari.terminalparam;

/**
 * 终端安装数据值对象
 * @author 姜炜超
 */
public class TerminalInstallBean implements java.io.Serializable {
    private String orgNo;//供电单位编号
    private String orgName;//供电单位名称
	private Long totalNum;//总数
	private Long succNum;//测试成功 100
	private Long failedNum;//测试失败 -100
	private Long initNum;//初始状态(未测试) 0
	private Long dealingNum;//测试中 1
	private Long synFailedNum;//建档失败 -1
	private String statusName;//状态名，比如，成功、失败等等
	private Long statusNum;//状态数量，比如，成功数量、失败数量等等
	private Long succCountNum;//下属成功数
	private Long failCountNum;//下属失败数
	
	public String getOrgNo() {
		return orgNo;
	}
	public void setOrgNo(String orgNo) {
		this.orgNo = orgNo;
	}
	public String getOrgName() {
		return orgName;
	}
	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}
	public Long getTotalNum() {
		return totalNum;
	}
	public void setTotalNum(Long totalNum) {
		this.totalNum = totalNum;
	}
	public Long getSuccNum() {
		return succNum;
	}
	public void setSuccNum(Long succNum) {
		this.succNum = succNum;
	}
	public Long getFailedNum() {
		return failedNum;
	}
	public void setFailedNum(Long failedNum) {
		this.failedNum = failedNum;
	}
	public Long getInitNum() {
		return initNum;
	}
	public void setInitNum(Long initNum) {
		this.initNum = initNum;
	}
	public Long getDealingNum() {
		return dealingNum;
	}
	public void setDealingNum(Long dealingNum) {
		this.dealingNum = dealingNum;
	}
	public Long getSynFailedNum() {
		return synFailedNum;
	}
	public void setSynFailedNum(Long synFailedNum) {
		this.synFailedNum = synFailedNum;
	}
	public String getStatusName() {
		return statusName;
	}
	public void setStatusName(String statusName) {
		this.statusName = statusName;
	}
	public Long getStatusNum() {
		return statusNum;
	}
	public void setStatusNum(Long statusNum) {
		this.statusNum = statusNum;
	}
	public Long getSuccCountNum() {
		return succCountNum;
	}
	public void setSuccCountNum(Long succCountNum) {
		this.succCountNum = succCountNum;
	}
	public Long getFailCountNum() {
		return failCountNum;
	}
	public void setFailCountNum(Long failCountNum) {
		this.failCountNum = failCountNum;
	}
}
