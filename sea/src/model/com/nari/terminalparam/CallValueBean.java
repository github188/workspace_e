package com.nari.terminalparam;

public class CallValueBean {
	private String unit;
	private String statusCode;
	private String blockSn;
	private String tmnlAssetNo;
	private String currentValue;
	private String callValue;
	private String protItemName;
	private String mpSn;
	
	
	public String getMpSn() {
		return mpSn;
	}
	public void setMpSn(String mpSn) {
		this.mpSn = mpSn;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	public String getStatusCode() {
		return statusCode;
	}
	public void setStatusCode(String statusCode) {
		this.statusCode = statusCode;
	}
	public String getBlockSn() {
		return blockSn;
	}
	public void setBlockSn(String blockSn) {
		this.blockSn = blockSn;
	}
	public String getTmnlAssetNo() {
		return tmnlAssetNo;
	}
	public void setTmnlAssetNo(String tmnlAssetNo) {
		this.tmnlAssetNo = tmnlAssetNo;
	}
	public String getCurrentValue() {
		return currentValue;
	}
	public void setCurrentValue(String currentValue) {
		this.currentValue = currentValue;
	}
	public String getCallValue() {
		return callValue;
	}
	public void setCallValue(String callValue) {
		this.callValue = callValue;
	}
	public String getProtItemName() {
		return protItemName;
	}
	public void setProtItemName(String protItemName) {
		this.protItemName = protItemName;
	}
	
	
	
}
