package com.nari.terminalparam;

public class TTaskTemplateBean {
	private   String templateId;
	private   String templateName;
	private   String taskPropertyName;
	private   String dataName;
	
	
	public String getTemplateId() {
		return templateId;
	}
	public void setTemplateId(String templateId) {
		this.templateId = templateId;
	}
	public String getTemplateName() {
		return templateName;
	}
	public void setTemplateName(String templateName) {
		this.templateName = templateName;
	}
	public String getTaskPropertyName() {
		return taskPropertyName;
	}
	public void setTaskPropertyName(String taskPropertyName) {
		this.taskPropertyName = taskPropertyName;
	}
	public String getDataName() {
		return dataName;
	}
	public void setDataName(String dataName) {
		this.dataName = dataName;
	}
	
	
}