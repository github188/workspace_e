package com.nari.terminalparam;

/**
 * 终端调试信息数据值对象
 * @author 姜炜超
 */
public class TerminalDebugInfoBean implements java.io.Serializable {
	private String appNo;//申请单号
	private String tmnlAddr;//终端地址
    private String meterNo;//电能表资产号
    private String failCause;//失败原因
    private String debugProcess;//调试进度
    private String debugTime;//调试时间
    private String flowNode;//流程节点
    private String flowStatus;//流程状态
    private String mpSnList;//测量点列表
    private String flowNo;//流程状态码
    
    
	/**
	 *  the flowNo
	 * @return the flowNo
	 */
	public String getFlowNo() {
		return flowNo;
	}
	/**
	 *  the flowNo to set
	 * @param flowNo the flowNo to set
	 */
	public void setFlowNo(String flowNo) {
		this.flowNo = flowNo;
	}
	public String getAppNo() {
		return appNo;
	}
	public void setAppNo(String appNo) {
		this.appNo = appNo;
	}
	public String getTmnlAddr() {
		return tmnlAddr;
	}
	public void setTmnlAddr(String tmnlAddr) {
		this.tmnlAddr = tmnlAddr;
	}
	public String getMeterNo() {
		return meterNo;
	}
	public void setMeterNo(String meterNo) {
		this.meterNo = meterNo;
	}
	public String getFailCause() {
		return failCause;
	}
	public void setFailCause(String failCause) {
		this.failCause = failCause;
	}
	public String getDebugProcess() {
		return debugProcess;
	}
	public void setDebugProcess(String debugProcess) {
		this.debugProcess = debugProcess;
	}
	public String getDebugTime() {
		return debugTime;
	}
	public void setDebugTime(String debugTime) {
		this.debugTime = debugTime;
	}
	public String getFlowNode() {
		return flowNode;
	}
	public void setFlowNode(String flowNode) {
		this.flowNode = flowNode;
	}
	public String getFlowStatus() {
		return flowStatus;
	}
	public void setFlowStatus(String flowStatus) {
		this.flowStatus = flowStatus;
	}
	public String getMpSnList() {
		return mpSnList;
	}
	public void setMpSnList(String mpSnList) {
		this.mpSnList = mpSnList;
	}
}
