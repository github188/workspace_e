package com.nari.advapp.vipconsman;

/**
 * 重点用户监测数据之数据值对象
 * @author 姜炜超
 */
public class VipConsMonitorDataBean {
	private String orgNo;//供电单位编号
    private String orgName;//供电单位名称
    private String consNo;//用户编号
    private String consName;//用户名
	private String loadCurve;//负荷曲线
    private String energyCurve;//电能量曲线
    private String runStatus;//实时工况
    private String abnormalEvent;//异常事件
    
	public String getOrgNo() {
		return orgNo;
	}
	public void setOrgNo(String orgNo) {
		this.orgNo = orgNo;
	}
	public String getOrgName() {
		return orgName;
	}
	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}
	public String getConsNo() {
		return consNo;
	}
	public void setConsNo(String consNo) {
		this.consNo = consNo;
	}
	public String getConsName() {
		return consName;
	}
	public void setConsName(String consName) {
		this.consName = consName;
	}
	public String getLoadCurve() {
		return loadCurve;
	}
	public void setLoadCurve(String loadCurve) {
		this.loadCurve = loadCurve;
	}
	public String getEnergyCurve() {
		return energyCurve;
	}
	public void setEnergyCurve(String energyCurve) {
		this.energyCurve = energyCurve;
	}
	public String getRunStatus() {
		return runStatus;
	}
	public void setRunStatus(String runStatus) {
		this.runStatus = runStatus;
	}
	public String getAbnormalEvent() {
		return abnormalEvent;
	}
	public void setAbnormalEvent(String abnormalEvent) {
		this.abnormalEvent = abnormalEvent;
	}
}
