package com.nari.advapp.vipconsman;

import java.io.Serializable;

/**
 * 
 * 重点用户主键
 * 
 * @author ChunMingLi
 * @since 2010-6-2
 * 
 */
public class VipConsManagerId implements Serializable{
	// 供电单位
	private String orgNo;

	// 用户标识
	private long consId;
	
	

	/**
	 * default constructor
	 */
	public VipConsManagerId() {
	}

	/**
	 * full constructor
	 */
	public VipConsManagerId(String orgNo, long consId) {
		super();
		this.orgNo = orgNo;
		this.consId = consId;
	}

	/**
	 *  the orgNo
	 * @return the orgNo
	 */
	public String getOrgNo() {
		return orgNo;
	}

	/**
	 *  the orgNo to set
	 * @param orgNo the orgNo to set
	 */
	public void setOrgNo(String orgNo) {
		this.orgNo = orgNo;
	}

	/**
	 *  the consId
	 * @return the consId
	 */
	public long getConsId() {
		return consId;
	}

	/**
	 *  the consId to set
	 * @param consId the consId to set
	 */
	public void setConsId(long consId) {
		this.consId = consId;
	}
	
}
