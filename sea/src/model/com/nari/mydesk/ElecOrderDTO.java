public class ElecOrderDTO {

	private int order ;
	private String cons_no;
	private String cons_name;
	private String org_name;
	private Double pap_e;
	public int getOrder() {
		return order;
	}
	public void setOrder(int order) {
		this.order = order;
	}
	
	public String getCons_no() {
		return cons_no;
	}
	public void setCons_no(String consNo) {
		cons_no = consNo;
	}
	public String getCons_name() {
		return cons_name;
	}
	public void setCons_name(String consName) {
		cons_name = consName;
	}
	public String getOrg_name() {
		return org_name;
	}
	public void setOrg_name(String orgName) {
		org_name = orgName;
	}
	public Double getPap_e() {
		return pap_e;
	}
	public void setPap_e(Double papE) {
		pap_e = papE;
	}
	
	
}
