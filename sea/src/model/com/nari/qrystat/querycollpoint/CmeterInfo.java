package com.nari.qrystat.querycollpoint;

public class CmeterInfo {
	private   String cpNo;
	private   String name;
	private   String cpTypeCode;
	private   String statusCode;
	private   String cpAddr;
	private   String gpsLongitude;
	private   String gpsLatitude;
	private   String rtu;
	public String getCpNo() {
		return cpNo;
	}
	public void setCpNo(String cpNo) {
		this.cpNo = cpNo;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCpTypeCode() {
		return cpTypeCode;
	}
	public void setCpTypeCode(String cpTypeCode) {
		this.cpTypeCode = cpTypeCode;
	}
	public String getStatusCode() {
		return statusCode;
	}
	public void setStatusCode(String statusCode) {
		this.statusCode = statusCode;
	}
	public String getCpAddr() {
		return cpAddr;
	}
	public void setCpAddr(String cpAddr) {
		this.cpAddr = cpAddr;
	}
	public String getGpsLongitude() {
		return gpsLongitude;
	}
	public void setGpsLongitude(String gpsLongitude) {
		this.gpsLongitude = gpsLongitude;
	}
	public String getGpsLatitude() {
		return gpsLatitude;
	}
	public void setGpsLatitude(String gpsLatitude) {
		this.gpsLatitude = gpsLatitude;
	}
	public String getRtu() {
		return rtu;
	}
	public void setRtu(String rtu) {
		this.rtu = rtu;
	}
	

}
