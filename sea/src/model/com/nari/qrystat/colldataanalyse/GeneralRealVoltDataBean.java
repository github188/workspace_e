package com.nari.qrystat.colldataanalyse;

import java.io.Serializable;

public class GeneralRealVoltDataBean implements Serializable{
	private String time;
    private Double voltA;
    private Double voltB;
    private Double voltC;
    private Boolean flagA;
    private Boolean flagB;
    private Boolean flagC;
    private Boolean beanFlag;//对于整个bean是否漏点的标识
    
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public Double getVoltA() {
		return voltA;
	}
	public void setVoltA(Double voltA) {
		this.voltA = voltA;
	}
	public Double getVoltB() {
		return voltB;
	}
	public void setVoltB(Double voltB) {
		this.voltB = voltB;
	}
	public Double getVoltC() {
		return voltC;
	}
	public void setVoltC(Double voltC) {
		this.voltC = voltC;
	}
	public Boolean getFlagA() {
		return flagA;
	}
	public void setFlagA(Boolean flagA) {
		this.flagA = flagA;
	}
	public Boolean getFlagB() {
		return flagB;
	}
	public void setFlagB(Boolean flagB) {
		this.flagB = flagB;
	}
	public Boolean getFlagC() {
		return flagC;
	}
	public void setFlagC(Boolean flagC) {
		this.flagC = flagC;
	}
	public Boolean getBeanFlag() {
		return beanFlag;
	}
	public void setBeanFlag(Boolean beanFlag) {
		this.beanFlag = beanFlag;
	}
}
