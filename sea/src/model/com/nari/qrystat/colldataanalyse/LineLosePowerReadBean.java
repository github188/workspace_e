package com.nari.qrystat.colldataanalyse;

import java.io.Serializable;

/**
 * 线路线损抄表成功失败数据值对象
 * @author 姜炜超
 */
public class LineLosePowerReadBean implements Serializable{
	private String lineId;//线路id
	private String consNo;//用户编号
	private String consName;//用户名称
	private String mpNo;//计量点编号
	private String mpName;//计量点名称
	private String date;//数据日期
	private String value;//数据值
	private String assetNo;//电表资产编号
	private String tmnlAssetNo;//终端资产编号
	
	public String getLineId() {
		return lineId;
	}
	public void setLineId(String lineId) {
		this.lineId = lineId;
	}
	public String getConsNo() {
		return consNo;
	}
	public void setConsNo(String consNo) {
		this.consNo = consNo;
	}
	public String getConsName() {
		return consName;
	}
	public void setConsName(String consName) {
		this.consName = consName;
	}
	public String getMpNo() {
		return mpNo;
	}
	public void setMpNo(String mpNo) {
		this.mpNo = mpNo;
	}
	public String getMpName() {
		return mpName;
	}
	public void setMpName(String mpName) {
		this.mpName = mpName;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public String getAssetNo() {
		return assetNo;
	}
	public void setAssetNo(String assetNo) {
		this.assetNo = assetNo;
	}
	public String getTmnlAssetNo() {
		return tmnlAssetNo;
	}
	public void setTmnlAssetNo(String tmnlAssetNo) {
		this.tmnlAssetNo = tmnlAssetNo;
	}
}
