package com.nari.qrystat.colldataanalyse;

/**
 * 行业用电趋势分析数据值对象
 * @author 姜炜超
 */
public class TradeTrendlineDataBean implements java.io.Serializable {
    private Double data;//数据
    private Boolean flag;//数据是否有值，true表示无值
    private String time;//日期时间
    
	public Double getData() {
		return data;
	}
	public void setData(Double data) {
		this.data = data;
	}
	public Boolean getFlag() {
		return flag;
	}
	public void setFlag(Boolean flag) {
		this.flag = flag;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
}
