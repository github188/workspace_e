package com.nari.qrystat.simquery;

public class SIMOverTraffic {
	private String orgNo;// 供电单位编号
	private String flowDate;// 统计日期
	private String consNo; // 用户编号
	private String consName; // 用户名称
	private String simNo; // SIM卡号
	private String cisAssetNo;// 终端资产编号
	private String terminalAddr; // 终端地址
	private String factoryName;// 终端厂商
	private Float allFlow; // 全部流量
	private Float overFlow; // 超流量
	private Float overFee; // 超流量费

	public String getOrgNo() {
		return orgNo;
	}

	public void setOrgNo(String orgNo) {
		this.orgNo = orgNo;
	}

	public String getFlowDate() {
		return flowDate;
	}

	public void setFlowDate(String flowDate) {
		this.flowDate = flowDate;
	}

	public String getConsNo() {
		return consNo;
	}

	public void setConsNo(String consNo) {
		this.consNo = consNo;
	}

	public String getConsName() {
		return consName;
	}

	public void setConsName(String consName) {
		this.consName = consName;
	}

	public String getSimNo() {
		return simNo;
	}

	public void setSimNo(String simNo) {
		this.simNo = simNo;
	}

	public String getCisAssetNo() {
		return cisAssetNo;
	}

	public void setCisAssetNo(String cisAssetNo) {
		this.cisAssetNo = cisAssetNo;
	}

	public String getTerminalAddr() {
		return terminalAddr;
	}

	public void setTerminalAddr(String terminalAddr) {
		this.terminalAddr = terminalAddr;
	}

	public String getFactoryName() {
		return factoryName;
	}

	public void setFactoryName(String factoryName) {
		this.factoryName = factoryName;
	}

	public Float getAllFlow() {
		return allFlow;
	}

	public void setAllFlow(Float allFlow) {
		this.allFlow = allFlow;
	}

	public Float getOverFlow() {
		return overFlow;
	}

	public void setOverFlow(Float overFlow) {
		this.overFlow = overFlow;
	}

	public Float getOverFee() {
		return overFee;
	}

	public void setOverFee(Float overFee) {
		this.overFee = overFee;
	}
}
