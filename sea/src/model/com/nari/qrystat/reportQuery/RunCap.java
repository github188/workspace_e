package com.nari.qrystat.reportQuery;

import java.io.Serializable;

public class RunCap implements Serializable {

	/**
	 * 运行容量
	 */
	private static final long serialVersionUID = 1L;
	//运行容量大小
	private float runCap;
	//运行容量等级
	private String capGrade;
	public float getRunCap() {
		return runCap;
	}
	public void setRunCap(float runCap) {
		this.runCap = runCap;
	}
	public String getCapGrade() {
		return capGrade;
	}
	public void setCapGrade(String capGrade) {
		this.capGrade = capGrade;
	}

}
