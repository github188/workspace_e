package com.nari.qrystat.reportQuery;
/*
 * @author 陈国章
 * @description 用来表达模板数据的GRID的信息
 */

public class ConsInfo {
	//表示供电单位
	private String org_name;
	//表示用户编号
	private String cons_no;
	//表示用户名称
	private String cons_name;
	//终端地址
	private String terminal_addr;
	//终端资产号
	private String tmnl_asset_no;
	public String getOrg_name() {
		return org_name;
	}
	public void setOrg_name(String orgName) {
		org_name = orgName;
	}
	public String getCons_no() {
		return cons_no;
	}
	public void setCons_no(String consNo) {
		cons_no = consNo;
	}
	public String getCons_name() {
		return cons_name;
	}
	public void setCons_name(String consName) {
		cons_name = consName;
	}
	public String getTerminal_addr() {
		return terminal_addr;
	}
	public void setTerminal_addr(String terminalAddr) {
		terminal_addr = terminalAddr;
	}
	public String getTmnl_asset_no() {
		return tmnl_asset_no;
	}
	public void setTmnl_asset_no(String tmnlAssetNo) {
		tmnl_asset_no = tmnlAssetNo;
	}
	
	
	
}
