package com.nari.baseapp.datagatherman;

/**
 * 设备监测DTO
 * @author 余涛
 */
public class DeviceMonitorDto {
	private String orgName;
	private String orgNo;
	
	//严重级别
	private int serisEvents;
	private int minorEvents;
	private int generEvents;
	
	//生成厂商
	private int[] factories;
	
	//FusionChart时JSON用到的属性
	private String types;
	private int value;
	
	public String getOrgName() {
		return orgName;
	}
	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}
	public String getOrgNo() {
		return orgNo;
	}
	public void setOrgNo(String orgNo) {
		this.orgNo = orgNo;
	}
	public int getSerisEvents() {
		return serisEvents;
	}
	public void setSerisEvents(int serisEvents) {
		this.serisEvents = serisEvents;
	}
	public int getMinorEvents() {
		return minorEvents;
	}
	public void setMinorEvents(int minorEvents) {
		this.minorEvents = minorEvents;
	}
	public int getGenerEvents() {
		return generEvents;
	}
	public void setGenerEvents(int generEvents) {
		this.generEvents = generEvents;
	}
	public int[] getFactories() {
		return factories;
	}
	public void setFactories(int[] factories) {
		this.factories = factories;
	}
	public String getTypes() {
		return types;
	}
	public void setTypes(String types) {
		this.types = types;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
}
