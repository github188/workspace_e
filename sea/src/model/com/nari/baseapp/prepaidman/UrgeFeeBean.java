package com.nari.baseapp.prepaidman;

public class UrgeFeeBean {
    private String orgName;  //供电单位名称
    private String appNo;     //供电单号
    private String consNo;    //用户编号
    private String consName;  //用户名称
    private String terminalAddr;  //终端地址
    private String ctrlType;      //控制类型
    private String ctrlStatus;    //控制状态
    private String ctrlTime;      //控制时间
    
	public String getOrgName() {
		return orgName;
	}
	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}
	public String getAppNo() {
		return appNo;
	}
	public void setAppNo(String appNo) {
		this.appNo = appNo;
	}
	public String getConsNo() {
		return consNo;
	}
	public void setConsNo(String consNo) {
		this.consNo = consNo;
	}
	public String getConsName() {
		return consName;
	}
	public void setConsName(String consName) {
		this.consName = consName;
	}
	public String getTerminalAddr() {
		return terminalAddr;
	}
	public void setTerminalAddr(String terminalAddr) {
		this.terminalAddr = terminalAddr;
	}
	public String getCtrlType() {
		return ctrlType;
	}
	public void setCtrlType(String ctrlType) {
		this.ctrlType = ctrlType;
	}
	public String getCtrlStatus() {
		return ctrlStatus;
	}
	public void setCtrlStatus(String ctrlStatus) {
		this.ctrlStatus = ctrlStatus;
	}
	public String getCtrlTime() {
		return ctrlTime;
	}
	public void setCtrlTime(String ctrlTime) {
		this.ctrlTime = ctrlTime;
	}
    
}
