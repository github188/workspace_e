package com.nari.baseapp.prepaidman;

/**
 * 预付费投入调试总加组下属测量点数据值对象
 * @author 姜炜超
 */
public class PPDebugTotalMpBean {
    private Integer mpSn;//测量点编号
    private String mpType;//测量点类型
    private Short calcFlag;//0-加运算，1-减运算
    private String calcFlagName;//加或减
    private String pulseArrtName;//正向或反向
    private String pulseAttr;//脉冲属性，1正向or2反向
    private String cmpNo;//计量点编号
    private String cmpName;//计量点名称
    private String totalName;//总加组名称
    
	public Integer getMpSn() {
		return mpSn;
	}
	public void setMpSn(Integer mpSn) {
		this.mpSn = mpSn;
	}
	public String getMpType() {
		return mpType;
	}
	public void setMpType(String mpType) {
		this.mpType = mpType;
	}
	public Short getCalcFlag() {
		return calcFlag;
	}
	public void setCalcFlag(Short calcFlag) {
		this.calcFlag = calcFlag;
	}
	public String getPulseAttr() {
		return pulseAttr;
	}
	public void setPulseAttr(String pulseAttr) {
		this.pulseAttr = pulseAttr;
	}
	public String getCmpNo() {
		return cmpNo;
	}
	public void setCmpNo(String cmpNo) {
		this.cmpNo = cmpNo;
	}
	public String getCmpName() {
		return cmpName;
	}
	public void setCmpName(String cmpName) {
		this.cmpName = cmpName;
	}
	public String getCalcFlagName() {
		return calcFlagName;
	}
	public void setCalcFlagName(String calcFlagName) {
		this.calcFlagName = calcFlagName;
	}
	public String getPulseArrtName() {
		return pulseArrtName;
	}
	public void setPulseArrtName(String pulseArrtName) {
		this.pulseArrtName = pulseArrtName;
	}
	public String getTotalName() {
		return totalName;
	}
	public void setTotalName(String totalName) {
		this.totalName = totalName;
	}
}
