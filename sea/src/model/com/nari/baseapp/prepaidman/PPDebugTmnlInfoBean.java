package com.nari.baseapp.prepaidman;

/**
 * 预付费投入调试终端工况信息数据值对象
 * @author 姜炜超
 */
public class PPDebugTmnlInfoBean {
	private String terminalId;//终端id
	private String tmnlAssetNo;//终端资产编号
	private String cpNo;//采集点编号
	private String cpAddr;//采集点地址
	private Integer isOnline;//终端在线
	private String powerPaulCode;//保电投入编码 null表示未知，0表示没有投入，1表示保电投入
	private String powerPaulStatus;//保电投入状态
	private String commStauts;//与电能表通讯状态
	
	public String getTerminalId() {
		return terminalId;
	}
	public void setTerminalId(String terminalId) {
		this.terminalId = terminalId;
	}
	public String getTmnlAssetNo() {
		return tmnlAssetNo;
	}
	public void setTmnlAssetNo(String tmnlAssetNo) {
		this.tmnlAssetNo = tmnlAssetNo;
	}
	public String getCpNo() {
		return cpNo;
	}
	public void setCpNo(String cpNo) {
		this.cpNo = cpNo;
	}
	public String getCpAddr() {
		return cpAddr;
	}
	public void setCpAddr(String cpAddr) {
		this.cpAddr = cpAddr;
	}
	public Integer getIsOnline() {
		return isOnline;
	}
	public void setIsOnline(Integer isOnline) {
		this.isOnline = isOnline;
	}
	public String getPowerPaulCode() {
		return powerPaulCode;
	}
	public void setPowerPaulCode(String powerPaulCode) {
		this.powerPaulCode = powerPaulCode;
	}
	public String getPowerPaulStatus() {
		return powerPaulStatus;
	}
	public void setPowerPaulStatus(String powerPaulStatus) {
		this.powerPaulStatus = powerPaulStatus;
	}
	public String getCommStauts() {
		return commStauts;
	}
	public void setCommStauts(String commStauts) {
		this.commStauts = commStauts;
	}
}
