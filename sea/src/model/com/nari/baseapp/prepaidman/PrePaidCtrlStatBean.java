package com.nari.baseapp.prepaidman;

public class PrePaidCtrlStatBean {
	private String orgNo;				//供电单位编号
	private String orgName;             //供电单位名称
	private long prePaidNum;            //预付费汇总数
	private long stationPrePaidNum;     //主站预付费户数
	private long tmnlPrePaidNum;        //终端预付费户数
	private long meterPrePaidNum;       //电表预付费户数
	
	public String getOrgNo() {
		return orgNo;
	}
	public void setOrgNo(String orgNo) {
		this.orgNo = orgNo;
	}
	public String getOrgName() {
		return orgName;
	}
	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}
	public long getPrePaidNum() {
		return prePaidNum;
	}
	public void setPrePaidNum(long prePaidNum) {
		this.prePaidNum = prePaidNum;
	}
	public long getStationPrePaidNum() {
		return stationPrePaidNum;
	}
	public void setStationPrePaidNum(long stationPrePaidNum) {
		this.stationPrePaidNum = stationPrePaidNum;
	}
	public long getTmnlPrePaidNum() {
		return tmnlPrePaidNum;
	}
	public void setTmnlPrePaidNum(long tmnlPrePaidNum) {
		this.tmnlPrePaidNum = tmnlPrePaidNum;
	}
	public long getMeterPrePaidNum() {
		return meterPrePaidNum;
	}
	public void setMeterPrePaidNum(long meterPrePaidNum) {
		this.meterPrePaidNum = meterPrePaidNum;
	}
	
}
