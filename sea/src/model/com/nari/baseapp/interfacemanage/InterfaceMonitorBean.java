package com.nari.baseapp.interfacemanage;

import java.util.Date;
import java.util.List;

/**
 * 
 * 接口监视表单Bean
 * 
 * @author ChunMingLi
 * @since  2010-6-28
 *
 */
public class InterfaceMonitorBean {
	// 业务类型
	private String businessType;
	
	private List<String> bustype;
	
	// 监视接口类型
	private String monitorType;
	//开始时间
	private Date startDate;
	//结束时间
	private Date endDate;
	
	
	/**
	 *  the bustype
	 * @return the bustype
	 */
	public List<String> getBustype() {
		return bustype;
	}
	/**
	 *  the bustype to set
	 * @param bustype the bustype to set
	 */
	public void setBustype(List<String> bustype) {
		this.bustype = bustype;
	}
	
	/**
	 *  the businessType
	 * @return the businessType
	 */
	public String getBusinessType() {
		return businessType;
	}
	/**
	 *  the businessType to set
	 * @param businessType the businessType to set
	 */
	public void setBusinessType(String businessType) {
		this.businessType = businessType;
	}
	/**
	 *  the monitorType
	 * @return the monitorType
	 */
	public String getMonitorType() {
		return monitorType;
	}
	/**
	 *  the monitorType to set
	 * @param monitorType the monitorType to set
	 */
	public void setMonitorType(String monitorType) {
		this.monitorType = monitorType;
	}
	/**
	 *  the startDate
	 * @return the startDate
	 */
	public Date getStartDate() {
		return startDate;
	}
	/**
	 *  the startDate to set
	 * @param startDate the startDate to set
	 */
	public void setStartDate(Date startDate) {
		this.startDate = startDate;
	}
	/**
	 *  the endDate
	 * @return the endDate
	 */
	public Date getEndDate() {
		return endDate;
	}
	/**
	 *  the endDate to set
	 * @param endDate the endDate to set
	 */
	public void setEndDate(Date endDate) {
		this.endDate = endDate;
	}
	
}
