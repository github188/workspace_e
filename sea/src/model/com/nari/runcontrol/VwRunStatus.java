package com.nari.runcontrol;

public class VwRunStatus {
	private String runStatusCode;
	private String runStatusName;
	
	public String getRunStatusCode() {
		return runStatusCode;
	}
	public void setRunStatusCode(String runStatusCode) {
		this.runStatusCode = runStatusCode;
	}
	public String getRunStatusName() {
		return runStatusName;
	}
	public void setRunStatusName(String runStatusName) {
		this.runStatusName = runStatusName;
	}
}
