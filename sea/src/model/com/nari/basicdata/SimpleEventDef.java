package com.nari.basicdata;

public class SimpleEventDef {
	private String eventNo;
	private String itemNo;
	private Short protItemSn;
	private String itemName;
	private Short datan;
	private Byte explanDatan;
	private String dataFormat;
	private String bytes;
	private String dataStyle;
	private String occupyBits;
	private String blockRule;
	private String unit;
	public String getEventNo() {
		return eventNo;
	}
	public void setEventNo(String eventNo) {
		this.eventNo = eventNo;
	}
	public String getItemNo() {
		return itemNo;
	}
	public void setItemNo(String itemNo) {
		this.itemNo = itemNo;
	}
	public Short getProtItemSn() {
		return protItemSn;
	}
	public void setProtItemSn(Short protItemSn) {
		this.protItemSn = protItemSn;
	}
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public Short getDatan() {
		return datan;
	}
	public void setDatan(Short datan) {
		this.datan = datan;
	}
	public Byte getExplanDatan() {
		return explanDatan;
	}
	public void setExplanDatan(Byte explanDatan) {
		this.explanDatan = explanDatan;
	}
	public String getDataFormat() {
		return dataFormat;
	}
	public void setDataFormat(String dataFormat) {
		this.dataFormat = dataFormat;
	}
	public String getBytes() {
		return bytes;
	}
	public void setBytes(String bytes) {
		this.bytes = bytes;
	}
	public String getDataStyle() {
		return dataStyle;
	}
	public void setDataStyle(String dataStyle) {
		this.dataStyle = dataStyle;
	}
	public String getOccupyBits() {
		return occupyBits;
	}
	public void setOccupyBits(String occupyBits) {
		this.occupyBits = occupyBits;
	}
	public String getBlockRule() {
		return blockRule;
	}
	public void setBlockRule(String blockRule) {
		this.blockRule = blockRule;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	
	
	
}
