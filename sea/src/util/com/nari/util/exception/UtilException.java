public class UtilException extends BaseException {
	private static final long serialVersionUID = 5910370830309272811L;

	public UtilException() {
		super();
	}

	public UtilException(Exception e, String msg) {
		super(e, msg);
	}

	public UtilException(String msg) {
		super(msg);
	}

}
